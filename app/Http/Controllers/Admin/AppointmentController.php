    public function approval($id)
    {
        $appointment = Appointment::select('status')->where('id',$id)->first();
        
        if ($appointment->status==0){
            $status = 1;
        }else{
            $status = 0;
        }

        Appointment::where('id',$id)->update(['status'=> $status]);

        return redirect()->route('admin.appointment.index')->with('message', 'Appointment Approved Successfully');
